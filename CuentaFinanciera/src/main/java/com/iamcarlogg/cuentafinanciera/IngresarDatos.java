    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String titular = jTextField1.getText().trim();
        String cantidadStr = jTextField2.getText().trim();
        double cantidad = cantidadStr.isEmpty() ? 0 : Double.parseDouble(cantidadStr);

        if (titular.isEmpty()) {
            JOptionPane.showMessageDialog(this, "El titular es obligatorio.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        Cuenta nuevaCuenta = new Cuenta(titular, cantidad);
        cuentas.add(nuevaCuenta);

        JOptionPane.showMessageDialog(this, "Cuenta creada:\n" + nuevaCuenta.toString(), "Ã‰xito", JOptionPane.INFORMATION_MESSAGE);

        jTextField1.setText("");
        jTextField2.setText("");
        
    }//GEN-LAST:event_jButton1ActionPerformed

